package myapplication.example.com.healthlifehack;

import android.content.Intent;
import android.support.v7.app.ActionBarActivity;
import android.os.Bundle;
import android.view.Menu;
import android.view.MenuItem;
import android.view.View;
import android.view.View.OnClickListener;
import android.widget.Button;


public class MainActivity extends ActionBarActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        addButtonClickListener();
        addPrescButtonListener();


    }

    public void addButtonClickListener()
    {
        Button btnNavigator = (Button)findViewById(R.id.menuButton);
        btnNavigator.setOnClickListener(new OnClickListener(){
            public void onClick(View arg)
            {
                Intent intent = new Intent(MainActivity.this, myapplication.example.com.healthlifehack.Menu.class);
                startActivity(intent);
            }
        });


    }

    public void addPrescButtonListener()
    {
        Button btnPresc = (Button)findViewById(R.id.prescButton);
        btnPresc.setOnClickListener(new OnClickListener(){
            public void onClick(View arg)
            {
                Intent intent = new Intent(MainActivity.this, Notif.class);
                startActivity(intent);
            }
        });
    }




    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        // Inflate the menu; this adds items to the action bar if it is present.
        getMenuInflater().inflate(R.menu.main, menu);
        return true;
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        // Handle action bar item clicks here. The action bar will
        // automatically handle clicks on the Home/Up button, so long
        // as you specify a parent activity in AndroidManifest.xml.
        int id = item.getItemId();
        if (id == R.id.action_settings) {
            return true;
        }
        return super.onOptionsItemSelected(item);
    }
}
